#!/usr/bin/env python3
"""Check the signed-angle claims used in the proof, on configurations
generated by the converse construction of verify.py (which satisfy all the
original hypotheses, as verified there)."""
import math
from verify import sub, dot, cross, dist, circle3, circle_tangent_at, line_circle, power, ang, inside_tri

def signed_ray_angle(V, P, Q):
    """signed angle from ray V->P to ray V->Q in degrees, in (-180,180]."""
    u=sub(P,V); v=sub(Q,V)
    a=math.degrees(math.atan2(cross(u,v), dot(u,v)))
    return a

def build(A,B,C,x):
    M=((A[0]+B[0])/2,(A[1]+B[1])/2); N=((A[0]+C[0])/2,(A[1]+C[1])/2)
    b=dist(A,C); c=dist(A,B); y=c*x/b
    ux=sub(B,A); X=(A[0]+x*ux[0]/c, A[1]+x*ux[1]/c)
    uy=sub(C,A); Y=(A[0]+y*uy[0]/b, A[1]+y*uy[1]/b)
    omK=circle_tangent_at(M,ux,C) if abs(x-c/2)<1e-12 else circle3(C,M,X)
    dBY=sub(Y,B)
    K=[ (B[0]+t*dBY[0],B[1]+t*dBY[1]) for t in line_circle(B,dBY,omK[0],omK[1]) ]
    K=[k for k in K if inside_tri(k,(B,M,C))][0]
    omL=circle3(B,N,Y)
    dCX=sub(X,C)
    L=[ (C[0]+t*dCX[0],C[1]+t*dCX[1]) for t in line_circle(C,dCX,omL[0],omL[1]) ]
    L=[l for l in L if inside_tri(l,(B,N,C))][0]
    return M,N,X,Y,K,L

for (A,B,C,x) in [((0,0),(4,0),(1,3),1.2), ((0,0),(4,0),(1,3),2.0),
                  ((0,0),(5,0),(0.5,2.5),0.9), ((0,0),(3.2,0),(2.5,2.2),1.4)]:
    M,N,X,Y,K,L = build(A,B,C,x)
    th=ang(K,B,A); ph=ang(L,B,K); ps=ang(B,M,K)
    print(f"A={A} B={B} C={C} x={x}")
    print("  ccw check (should be >0):", cross(sub(B,A),sub(C,A)))
    print("  S1a: angle(BA->BK) = -theta :", signed_ray_angle(B,A,K), "vs -", th)
    print("  S1b: angle(BA->BL) = -(th+ph):", signed_ray_angle(B,A,L), "vs -", th+ph)
    print("  S2a: angle(CA->CL) = +theta :", signed_ray_angle(C,A,L), "vs +", th)
    print("  S2b: angle(CA->CK) = +(th+ps'):", signed_ray_angle(C,A,K), "vs +", th+ang(L,C,K))
    print("  S3 : angle(MB->MK) = +psi   :", signed_ray_angle(M,B,K), "vs +", ps)
    print("  S4 : angle(NC->NL) = -phi   :", signed_ray_angle(N,C,L), "vs -", ph)
    print("  ray-angle eq (BK->BL) vs (NC->NL):", signed_ray_angle(B,K,L), signed_ray_angle(N,C,L))
    print("  ray-angle eq (CL->CK) vs (MB->MK):", signed_ray_angle(C,L,K), signed_ray_angle(M,B,K))
    print()
