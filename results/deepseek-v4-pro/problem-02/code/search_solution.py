import random, math

random.seed(123)
# Fixed triangle
A = 0+0j
B = 4+1.5j
C = 1.0+3.0j
M = (A+B)/2
N = (A+C)/2

print(f"M={M}, N={N}")

def make_pts(s, t, u, v):
    K = (1-s-t)*B + s*M + t*C
    L = (1-u-v)*B + u*N + v*C
    return {'A':A,'B':B,'C':C,'M':M,'N':N,'K':K,'L':L}

def total_err(params):
    s, t, u, v = params
    if s < 0 or t < 0 or s+t > 1: return 1e9
    if u < 0 or v < 0 or u+v > 1: return 1e9
    pts = make_pts(s, t, u, v)
    e1, e2, e3 = errors(pts)
    return e1 + e2 + e3

# Simple random search + local refinement
best_err = float('inf')
best_params = None
for _ in range(50000):
    s = random.random()*0.8
    t = random.random()*(0.8 - s) if random.random()<0.5 else random.random()*0.5
    if t < 0: t = 0
    if s + t > 1:
        s, t = 0.5*s, 0.5*t
    u = random.random()*0.8
    v = random.random()*(0.8 - u) if random.random()<0.5 else random.random()*0.5
    if v < 0: v = 0
    if u + v > 1:
        u, v = 0.5*u, 0.5*v
    params = [s, t, u, v]
    err = total_err(params)
    if err < best_err:
        best_err = err
        best_params = params.copy()
        # print(f"err={err}: {params}")
    if best_err < 1e-8:
        break

print(f"Best error: {best_err}")
