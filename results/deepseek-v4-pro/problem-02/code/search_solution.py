def errors(pts):
    A=pts['A']; B=pts['B']; C=pts['C']; M=pts['M']; N=pts['N']; K=pts['K']; L=pts['L']
    
    # 1: ∠KBA = ∠ACL
    a1 = dir_angle(K, B, A)
    a2 = dir_angle(A, C, L)
    e1 = angle_diff(a1, a2)
    
    # 2: ∠LBK = ∠LNC
    a3 = dir_angle(L, B, K)
    a4 = dir_angle(L, N, C)
    e2 = angle_diff(a3, a4)
    
    # 3: ∠LCK = ∠BMK
    a5 = dir_angle(L, C, K)
    a6 = dir_angle(B, M, K)
    e3 = angle_diff(a5, a6)
    
    return e1, e2, e3

